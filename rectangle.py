def area(a, b):
    '''Принимает числа a и b (стороны прямоугольник)'''
    return a * b
'''Возвращает площадь прямоугольника со сторонами a и b'''
def perimeter(a, b):
    '''Принимает числа a и b (стороны прямоугольник)'''
    return (a + b)*2
