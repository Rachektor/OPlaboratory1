def area(a, h):
    '''Принимает числа a и h (основавние и высота треугольника)'''
    return a * h / 2
'''Возвращает площадь треугольника с основанием a и высотой h'''
def perimeter(a, b, c):
    '''Принимает числа a, b, c (стороны треугольника)'''
    return a + b + c
'''Возвращает периметр треугольника со сторонами a, b, c'''
